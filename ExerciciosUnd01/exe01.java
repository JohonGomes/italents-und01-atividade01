package ExerciciosUnd01;
/*
 Exercício 1: Variáveis Primitivas
 Crie um programa que declare variáveis para armazenar um número inteiro, um número de
 ponto flutuante e uma string. Atribua valores a essas variáveis e exiba-as na tela.
 */

public class exe01 {

    public static void main(String[] args) {
        int idade = 37;  // variavel inteira
        double peso = 69.50f; // variavel flutuante
        String nome = "Johon"; // variavel string

        // Mostrando os valores
        System.out.println("Ola meu nome é " + nome + " tenho " + idade + " anos e peso " + peso +" quilos" );
    }
}

