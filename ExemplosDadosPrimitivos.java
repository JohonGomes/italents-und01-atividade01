
public class ExemplosDadosPrimitivos {

	public static void main(String[] args) {
		
		//numeros inteiros
		byte valorByte = 127; //byte é um valor numérico - 127 é o max que o byte aceita.
		short valorShort = 32767; // -32768 até 32767
		int valorInt = 2147483647; // o max aceitavel
		long valorLong = 9223372036854775807L; //calculos matematicos complexos, precisamos colocar um L no final
		
		
		//constante - uma valor que não será alterado
		final float valorConstante = 3.141516f;
		
		
		//numeros flutuantes
		float valorFloat = 3.14151617f; // precisa-se do f no final para definir como float.
		double valorDouble = 3.14159265359;
		
		
		//tipo caracter
		char caracter = 1; //aceita um número ou letra com aspas simples se for usar letras.
		
		
		//tipo booleano
		boolean verdadeiro = true;
		boolean falso = false;
		
	}

}
